const map: { [x: string]: boolean } = { };
STOPWORDS.forEach(w => { map[w] = true; });
const replaceMap: { [x: string]: string } = {
    'maslow': 'Maslo',
};
export function isReplace(w: string) {
    return replaceMap[w.toLowerCase()];
}
export function isStopWord(w: string) {
    return !!map[w.toLowerCase()];
}
